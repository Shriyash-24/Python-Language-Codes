# Image - Represented by rows and columns and determined by pixel values.
# Types of Images - ->
# 1. Binary Image --> Image depicted by 0 (black) and 1 (white)
# 2. Black and White Image --> Black and white mixes together.
# 3. 1-bit - 2 colours, 2-bit - 4 colors, and so on.. 8-bit, 16-bit, 24-bit, 32-bit images.
# 8-Bit --> 00000000 --> Black, 00000001 --> Black with less darkness, 11111111 --> White.
# In 8-bit, you have 256 colours. RED - 0-255, BLUE - 0-255, GREEN - 0-255.
# Image formats --> .jpg/.jpeg, .png, .gif, .bmp, .eps, .tif/.tiff
