# IP ADDRESS - An internet protocol address (IP address) is a numerical label assigned to each device,
# which is connected to a computer network that uses the Internet Protocol for communication.

# Websites are located on server and each server also has an IP Address.
# google.com has an IP address and we can connect to Google using IP.

# How to find a website's IP - ping google.com (in cmd)
# Domain name checks for IP and it is called domain name resolution.

# Static V/S Dynamic IP -->
# 1. Dynamic IP - Which changes.
# 2. Static IP - Remains same.
# ISP assigns dynamic IP to your device.
# Static IP's are used by web servers. (Eg Google)
# Public IP V/s Private IP
# Local Network - You connect multiple devices to router/modem.
# Public Network - Internet to FB, Google etc.
# Inside local network, private IP is used. When facebook.com communicates with you, it will communicate via public IP of you.
# Public IP are used to connect to public network.
# Private IP are used for communication within local network.
# Public IP is assigned to router/modem which directly connects to public network.
# Private IP are assigned to individual devices in network.
