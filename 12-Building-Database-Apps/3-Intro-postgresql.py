# PostgreSQL
# Types of databases - Hierarchical, NoSQL Database, Object Oriented Database, Relational, Network database.

# Relational Databases -->
# 1. Categorized by a set of tables.
# 2. SQL (Structured Query Language) acts as the application interface.
# 3. Easier to modify ~ Extending, Joining.

# PostgreSQL
# 1. Object-Oriented Database + Relational database.
# 2. Similar to relational database.
# 3. Object Oriented Database --> Objects, classes and Inheritance are supported.

