# Syntax of Indexing - string[index]
# Syntax Of String - string[start:end:step]
# start - starting index at which slicing begins
# end - Ending index at which slicing ends (excluded)
# step - integer that specifies the increment/decrement of slicing
print(s1[2:7:1])
print(s1[2:9:2])
print(s1[1:12:3])

