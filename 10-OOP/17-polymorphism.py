# Polymorphism is an existence of entity in more than one form, characteristic of entity to be present in more than one form.

# If you apply + on 2 integers it will do addition.
# When you change data type of strings, it becomes concatenation. Joining of 2 strings together.
# So it looks the same for both data-types, but behaves differently.

a = 10
b = 20
print(a+b) # 30

s1 = "Hello"
s2 = "Hi"
print(s1+s2) # HelloHi
